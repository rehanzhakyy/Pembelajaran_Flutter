void main() {
  // String
  // Digunakan untuk menampung nilai String
  String keyboard = "Rexus";
  print(keyboard);

  // Bool
  // Digunakan untuk menampung nilai Boolean True / False
  bool isMechanical = true;
  print("Apakah Rexus termasuk keyboard mekanikal? $isMechanical");

  // Num
  // Digunakan untuk menampung nilai angka bilangan bulat bahkan desimal
  num percent = 65;
  print(percent);

  // Double
  // Digunakan untuk menampung nilai desimal
  double umur = 23.3;
  print(umur);

  // Int
  // Digunakan untuk menampung nilai bilangan bulat
  int nomorPunggung = 16;
  print(nomorPunggung);

  // List
  // Digunakan untuk menampung List
  List<String> mechKeyboard = ['Rexus', 'Red Dragon', 'Noir'];
  print(mechKeyboard);

  // Dynamic
  // Digunakan untuk menampung list dengan berbagai tipe data
  List<dynamic> favKeyboard = ['Noir', 'Mecha Dragon', 'Rexus'];
  print(favKeyboard);

  // Map
  // Digunakan untuk menampung nilai seperti format Json
  Map laptop = {"biasa": "Asus Vivobook", "gaming": "Asus TUF Gaming"};
  print(laptop);
}
